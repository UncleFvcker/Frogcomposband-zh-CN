const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const TSV = path.join(ROOT, "localization", "lib_edit_text_to_translate.tsv");
const OUT = path.join(ROOT, "src", "artifact_name_zh.inc");

function parseTsvLine(line) {
  const cols = [];
  let cur = "";
  for (let i = 0; i < line.length; i++) {
    const ch = line[i];
    if (ch === "\t") {
      cols.push(cur);
      cur = "";
    } else {
      cur += ch;
    }
  }
  cols.push(cur);
  return cols;
}

function cString(value) {
  return value
    .replace(/\\/g, "\\\\")
    .replace(/"/g, "\\\"")
    .replace(/\r/g, "\\r")
    .replace(/\n/g, "\\n");
}

const lines = fs.readFileSync(TSV, "utf8").split(/\r?\n/).filter(Boolean);
const header = parseTsvLine(lines.shift());
const idx = Object.fromEntries(header.map((h, i) => [h, i]));
const required = ["file", "col", "source", "zh_cn", "context"];

for (const key of required) {
  if (!(key in idx)) throw new Error(`Missing TSV column: ${key}`);
}

const names = [];
for (const line of lines) {
  const cols = parseTsvLine(line);
  if (cols[idx.file] !== "lib/edit/a_info.txt") continue;
  if (cols[idx.col] !== "N:name") continue;

  const zh = (cols[idx.zh_cn] || "").trim();
  if (!zh) continue;

  const m = /^N:(\d+):/.exec(cols[idx.context] || "");
  if (!m) continue;

  const id = Number(m[1]);
  names[id] = zh;
}

const max = names.length - 1;
const out = [];

out.push("/* This file is generated by tools/generate-artifact-name-translations.js.");
out.push(" * Do not edit by hand. Keep lib/edit/a_info.txt N:name values in English");
out.push(" * for parser lookups; this table is display-only localization.");
out.push(" */");
out.push("");
out.push("static cptr _artifact_name_zh[] =");
out.push("{");
for (let i = 0; i <= max; i++) {
  out.push(`    ${names[i] ? `"${cString(names[i])}"` : "NULL"},`);
}
out.push("};");
out.push("");

fs.writeFileSync(OUT, out.join("\n"), "utf8");
console.log(`Wrote ${path.relative(ROOT, OUT)} with ${names.filter(Boolean).length} translated artifact names.`);
