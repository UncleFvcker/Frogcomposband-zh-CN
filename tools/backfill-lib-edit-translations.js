const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const TSV = path.join(ROOT, "localization", "lib_edit_text_to_translate.tsv");
const REPORT = path.join(ROOT, "localization", "lib_edit_backfill_report.json");
const RESET_FROM_HEAD = process.argv.includes("--reset-from-head");

const DISPLAY_NAME_FIELDS = new Set([
  "lib/edit/a_info.txt\tN:name",
  "lib/edit/b_info.txt\tN:name",
  "lib/edit/d_info.txt\tN:name",
  "lib/edit/e_info.txt\tN:name",
  "lib/edit/k_info.txt\tN:name",
  "lib/edit/k_info.txt\tN:flavor",
  "lib/edit/q_info.txt\tN:name",
  "lib/edit/r_info.txt\tN:name",
  "lib/edit/rooms.txt\tN:name",
  "lib/edit/vaults.txt\tN:name",
  "lib/edit/v_info.txt\tN:name",
]);

function parseTsvLine(line) {
  const cols = [];
  let cur = "";
  for (let i = 0; i < line.length; i++) {
    const ch = line[i];
    if (ch === "\t") {
      cols.push(cur);
      cur = "";
    } else {
      cur += ch;
    }
  }
  cols.push(cur);
  return cols;
}

function cString(value) {
  return value
    .replace(/\\/g, "\\\\")
    .replace(/"/g, "\\\"")
    .replace(/\r/g, "\\r")
    .replace(/\n/g, "\\n");
}

function tableLine(values, i) {
  return `    ${values[i] ? `"${cString(values[i])}"` : "NULL"},`;
}

function writeTable(fileName, symbol, values, comment) {
  const out = [];
  const max = values.length - 1;
  out.push(`/* This file is generated by tools/backfill-lib-edit-translations.js.`);
  out.push(` * ${comment}`);
  out.push(" */");
  out.push("");
  out.push(`static cptr ${symbol}[] =`);
  out.push("{");
  for (let i = 0; i <= max; i++) out.push(tableLine(values, i));
  out.push("};");
  out.push("");
  fs.writeFileSync(path.join(ROOT, "src", fileName), out.join("\n"), "utf8");
}

function writePairTable(fileName, symbol, pairs, comment) {
  const out = [];
  out.push(`/* This file is generated by tools/backfill-lib-edit-translations.js.`);
  out.push(` * ${comment}`);
  out.push(" */");
  out.push("");
  out.push(`static struct { cptr key; cptr value; } ${symbol}[] =`);
  out.push("{");
  for (const [key, value] of pairs) {
    out.push(`    {"${cString(key)}", "${cString(value)}"},`);
  }
  out.push("    {NULL, NULL},");
  out.push("};");
  out.push("");
  fs.writeFileSync(path.join(ROOT, "src", fileName), out.join("\n"), "utf8");
}

function parseIndexedContext(context) {
  const m = /^N:(\d+):/.exec(context || "");
  return m ? Number(m[1]) : null;
}

function buildKInfoLineIds() {
  const result = new Map();
  const raw = fs.readFileSync(path.join(ROOT, "lib/edit/k_info.txt"), "utf8");
  const lines = raw.split(/\r?\n/);
  let kIdx = 0;
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (!line.startsWith("N:")) continue;
    const m = /^N:([^:]+):/.exec(line);
    if (!m) continue;
    if (m[1] === "*") kIdx++;
    else kIdx = Number(m[1]);
    result.set(i + 1, kIdx);
  }
  return result;
}

function safeDirect(row) {
  const key = `${row.file}\t${row.col}`;
  if (DISPLAY_NAME_FIELDS.has(key)) return false;
  if (row.col === "D" || row.col === "E") return true;
  if (row.col.startsWith("B:")) return true;
  if (row.file === "lib/edit/w_info.txt" && row.col === "W:name") return true;
  return false;
}

function translatedLine(row) {
  if (row.col === "D" || row.col === "E") return `${row.col}:${row.zh_cn}`;

  if (row.file === "lib/edit/w_info.txt" && row.col === "W:name") {
    const parts = row.context.split(":");
    parts[parts.length - 1] = row.zh_cn;
    return parts.join(":");
  }

  if ((row.file === "lib/edit/rooms.txt" || row.file === "lib/edit/vaults.txt" || row.file === "lib/edit/v_info.txt") && row.col === "N:name") {
    return `N:${row.zh_cn}`;
  }

  if (row.col.startsWith("B:")) {
    const parts = row.context.split(":");
    const kind = parts[2];
    const field = row.col.split(":").pop();
    if (kind === "N") {
      if (field === "name") parts[3] = row.zh_cn;
      else if (field === "owner") parts[4] = row.zh_cn;
      else if (field === "owner_race") parts[5] = row.zh_cn;
    } else if (kind === "A") {
      parts[4] = row.zh_cn;
    }
    return parts.join(":");
  }

  return null;
}

function replaceLine(fileLines, row, nextLine) {
  const original = row.context;
  const oneBased = Number(row.line);
  const candidates = [];

  if (Number.isInteger(oneBased) && oneBased > 0) {
    for (let i = oneBased - 3; i <= oneBased + 1; i++) {
      if (i >= 0 && i < fileLines.length) candidates.push(i);
    }
  }

  for (const i of candidates) {
    if (fileLines[i] === original) {
      fileLines[i] = nextLine;
      return "updated";
    }
    if (fileLines[i] === nextLine) return "already";
  }

  if ((row.col === "D" || row.col === "E") && Number.isInteger(oneBased) && oneBased > 0) {
    const i = oneBased - 1;
    if (i >= 0 && i < fileLines.length && fileLines[i].startsWith(`${row.col}:`)) {
      fileLines[i] = nextLine;
      return "updated";
    }
  }

  const idx = fileLines.indexOf(original);
  if (idx >= 0) {
    fileLines[idx] = nextLine;
    return "updated";
  }

  if (fileLines.includes(nextLine)) return "already";

  if (row.col.startsWith("B:")) {
    const parts = original.split(":");
    const prefix = parts[2] === "N"
      ? `B:${parts[1]}:N:`
      : `B:${parts[1]}:A:${parts[3]}:`;
    for (let i = 0; i < fileLines.length; i++) {
      if (fileLines[i].startsWith(prefix)) {
        fileLines[i] = translatedLine({...row, context: fileLines[i]});
        return "updated";
      }
    }
  }

  if (row.file === "lib/edit/w_info.txt" && row.col === "W:name") {
    const parts = original.split(":");
    const prefix = parts.slice(0, -1).join(":") + ":";
    for (let i = 0; i < fileLines.length; i++) {
      if (fileLines[i].startsWith(prefix)) {
        fileLines[i] = translatedLine({...row, context: fileLines[i]});
        return "updated";
      }
    }
  }

  return "missing";
}

const text = fs.readFileSync(TSV, "utf8");
const tsvLines = text.split(/\r?\n/).filter(Boolean);
const header = parseTsvLine(tsvLines.shift());
const idx = Object.fromEntries(header.map((h, i) => [h, i]));
const rows = tsvLines.map(line => {
  const cols = parseTsvLine(line);
  return Object.fromEntries(header.map((h, i) => [h, cols[i] || ""]));
}).filter(row => row.zh_cn.trim());

const nameTables = {
  artifact: [],
  body: [],
  dungeon: [],
  ego: [],
  kind: [],
  kindFlavor: [],
  monster: [],
  quest: [],
  roomTemplate: new Map(),
};
const kInfoLineIds = buildKInfoLineIds();

function readHeadFile(file) {
  return require("child_process").execFileSync("git", ["show", `HEAD:${file}`], {
    cwd: ROOT,
    encoding: "utf8",
    maxBuffer: 50 * 1024 * 1024,
  });
}

for (const row of rows) {
  let id = parseIndexedContext(row.context);
  if (row.file === "lib/edit/k_info.txt" && (row.col === "N:name" || row.col === "N:flavor")) {
    id = kInfoLineIds.get(Number(row.line));
  }
  if (id == null) continue;
  if (row.file === "lib/edit/a_info.txt" && row.col === "N:name") nameTables.artifact[id] = row.zh_cn;
  else if (row.file === "lib/edit/b_info.txt" && row.col === "N:name") nameTables.body[id] = row.zh_cn;
  else if (row.file === "lib/edit/d_info.txt" && row.col === "N:name") nameTables.dungeon[id] = row.zh_cn;
  else if (row.file === "lib/edit/e_info.txt" && row.col === "N:name") nameTables.ego[id] = row.zh_cn;
  else if (row.file === "lib/edit/k_info.txt" && row.col === "N:name") nameTables.kind[id] = row.zh_cn;
  else if (row.file === "lib/edit/k_info.txt" && row.col === "N:flavor") nameTables.kindFlavor[id] = row.zh_cn;
  else if (row.file === "lib/edit/q_info.txt" && row.col === "N:name") nameTables.quest[id] = row.zh_cn;
  else if (row.file === "lib/edit/r_info.txt" && row.col === "N:name") nameTables.monster[id] = row.zh_cn;
}

for (const row of rows) {
  if ((row.file === "lib/edit/rooms.txt" || row.file === "lib/edit/vaults.txt" || row.file === "lib/edit/v_info.txt") && row.col === "N:name") {
    nameTables.roomTemplate.set(row.source, row.zh_cn);
  }
}

writeTable("artifact_name_zh.inc", "_artifact_name_zh", nameTables.artifact, "Display-only artifact names; keep a_info.txt N:name in English for parser lookups.");
writeTable("body_name_zh.inc", "_body_name_zh", nameTables.body, "Display-only body template names; keep b_info.txt N:name in English for monster body lookups.");
writeTable("dungeon_name_zh.inc", "_dungeon_name_zh", nameTables.dungeon, "Display-only dungeon names; keep d_info.txt N:name in English for quest W: lookups.");
writeTable("ego_name_zh.inc", "_ego_name_zh", nameTables.ego, "Display-only ego names; keep e_info.txt N:name in English for EGO() lookups.");
writeTable("kind_name_zh.inc", "_kind_name_zh", nameTables.kind, "Display-only object kind names; keep k_info.txt N:name in English for OBJ() lookups.");
writeTable("kind_flavor_zh.inc", "_kind_flavor_zh", nameTables.kindFlavor, "Display-only object flavor names; keep k_info.txt N:flavor in English for parser stability.");
writeTable("monster_name_zh.inc", "_monster_name_zh", nameTables.monster, "Display-only monster names; keep r_info.txt N:name in English for KILL()/MON() lookups.");
writeTable("quest_name_zh.inc", "_quest_name_zh", nameTables.quest, "Display-only quest names; keep q_info.txt N:name in English for internal quest logic.");
writePairTable("room_template_name_zh.inc", "_room_template_name_zh", nameTables.roomTemplate, "Display-only room/vault template names; keep N:name in English so v_info parsing stays ASCII-safe.");

const directRows = rows.filter(safeDirect);
const files = new Map();
const stats = {updated: 0, already: 0, missing: 0, displayLayer: rows.length - directRows.length};
const missing = [];
const displayLayerFiles = new Set(rows
  .filter(row => DISPLAY_NAME_FIELDS.has(`${row.file}\t${row.col}`))
  .map(row => row.file));

for (const row of directRows) {
  const abs = path.join(ROOT, row.file);
  if (!files.has(abs)) {
    let raw;
    if (RESET_FROM_HEAD) {
      try {
        raw = readHeadFile(row.file);
      } catch (e) {
        raw = fs.readFileSync(abs, "utf8");
      }
    } else {
      raw = fs.readFileSync(abs, "utf8");
    }
    files.set(abs, {raw, eol: raw.includes("\r\n") ? "\r\n" : "\n", lines: raw.split(/\r?\n/)});
  }
  const nextLine = translatedLine(row);
  if (!nextLine) {
    missing.push({id: row.id, file: row.file, col: row.col, reason: "no translatedLine"});
    stats.missing++;
    continue;
  }
  const file = files.get(abs);
  const result = replaceLine(file.lines, row, nextLine);
  stats[result]++;
  if (result === "missing") missing.push({id: row.id, file: row.file, line: row.line, col: row.col, context: row.context, expected: nextLine});
}

for (const [abs, file] of files) {
  const next = file.lines.join(file.eol);
  if (next !== file.raw) fs.writeFileSync(abs, next, "utf8");
}

if (RESET_FROM_HEAD) {
  for (const file of displayLayerFiles) {
    const abs = path.join(ROOT, file);
    if (files.has(abs)) continue;
    try {
      const raw = readHeadFile(file);
      fs.writeFileSync(abs, raw, "utf8");
    } catch (e) {
      /* The file might be generated or absent in older trees. */
    }
  }
}

const report = {
  stats,
  generatedTables: Object.fromEntries(Object.entries(nameTables).map(([key, values]) => [key, values instanceof Map ? values.size : values.filter(Boolean).length])),
  missing,
};

fs.writeFileSync(REPORT, JSON.stringify(report, null, 2), "utf8");
console.log(JSON.stringify(report.stats));
